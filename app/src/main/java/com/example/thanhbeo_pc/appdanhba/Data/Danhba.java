package com.example.thanhbeo_pc.appdanhba.Data;

public class Danhba {
    private int id;
    private String ten;
    private String sdt;
    private String gt;

    public Danhba() {
        this.id = id;
        this.ten = ten;
        this.sdt = sdt;
        this.gt = gt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getGt() {
        return gt;
    }

    public void setGt(String gt) {
        this.gt = gt;
    }
}
